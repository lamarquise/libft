#include "libft.h"

void	ft_strclr(char *s)
{
	int		a;

	a = 0;
	if (!s)
		return ;
	while (s[a])
	{
		if (s[a + 1])
		{
			++a;
			s[a - 1] = '\0';
		}
		else
		{
			s[a] = '\0';
			++a;
		}
	}
}
