#include "libft.h"

void	ft_putnbr(int n)
{
	long			nb;
	unsigned char	*ret;

	nb = n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ret = (unsigned char*)(nb % 10 + 48);
	write(1, &ret, 1);
}
