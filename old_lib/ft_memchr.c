#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	a;

	a = 0;
	while (a < n)
	{
		if ((unsigned char)c == ((unsigned char*)s)[a])
			return (&((unsigned char*)s)[a]);
		++a;
	}
	return (0);
}
