#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	a;

	a = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[a] && s2[a] && s1[a] == s2[a] && a < n)
		++a;
	if ((!s1[a] && !s2[a]) || a == n)
		return (1);
	return (0);
}
