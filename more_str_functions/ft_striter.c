#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		a;

	a = 0;
	if (!f)
		return ;
	if (!s)
		return ;
	while (s[a])
	{
		f(&s[a]);
		++a;
	}
}
