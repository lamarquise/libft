#include "libft.h"

void	ft_strswap(char **s1, char **s2)
{
	char	*tmp;

	tmp = NULL;
	if (!s1 || !s2)
		return ;
	tmp = *s1;
	*s1 = *s2;
	*s2 = tmp;
}
