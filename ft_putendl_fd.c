#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		a;

	a = 0;
	if (!fd || !s)
		return ;
	while ((char)s[a])
	{
		write(fd, (void*)&s[a], 1);
		++a;
	}
	write(fd, "\n", 1);
}
